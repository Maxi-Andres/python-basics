def saludar(nombre):
    """Función que devuelve un saludo personalizado."""
    return f"¡Hola, {nombre}!"

def suma(a, b):
    """Función que devuelve la suma de dos números."""
    return a + b

def producto(a, b):
    """Función que devuelve el producto de dos números."""
    return a * b
