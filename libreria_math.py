import math

# Obtener el valor de pi
print("Valor de pi:")
print(math.pi)
print()

# Obtener el valor de e
print("Valor de e:")
print(math.e)
print()

# Calcular la raíz cuadrada
print("Raíz cuadrada de 16:")
print(math.sqrt(16))
print()

# Calcular el logaritmo natural
print("Logaritmo natural de 10:")
print(math.log(10))
print()

# Calcular el logaritmo en base 10
print("Logaritmo en base 10 de 100:")
print(math.log10(100))
print()

# Calcular el seno
print("Seno de 45 grados (en radianes):")
print(math.sin(math.radians(45)))
print()

# Calcular el coseno
print("Coseno de 45 grados (en radianes):")
print(math.cos(math.radians(45)))
print()

# Calcular la tangente
print("Tangente de 45 grados (en radianes):")
print(math.tan(math.radians(45)))
print()

# Calcular el valor absoluto
print("Valor absoluto de -10:")
print(math.fabs(-10))
print()

# Redondear al entero más cercano
print("Redondear 5.67 al entero más cercano:")
print(round(5.67))
print()

# Redondear hacia abajo
print("Redondear hacia abajo 5.67:")
print(math.floor(5.67))
print()

# Redondear hacia arriba
print("Redondear hacia arriba 5.67:")
print(math.ceil(5.67))
print()

# Calcular el factorial
print("Factorial de 5:")
print(math.factorial(5))
print()

# Calcular la combinación (nCr)
print("Combinación de 5 elementos tomados de 2 en 2:")
print(math.comb(5, 2))
print()

# Calcular la permutación (nPr)
print("Permutación de 5 elementos tomados de 2 en 2:")
print(math.perm(5, 2))
print()

# Potencia
print("2 elevado a la 3:")
print(math.pow(2, 3))
print()

# Exponencial
print("Exponencial de 1 (e^1):")
print(math.exp(1))
print()

# Convertir radianes a grados
print("Convertir 1 radian a grados:")
print(math.degrees(1))
print()

# Convertir grados a radianes
print("Convertir 45 grados a radianes:")
print(math.radians(45))
print()

# 1. math.pi: Valor de π.
# 2. math.e: Valor de e.
# 3. math.sqrt(x): Raíz cuadrada de x.
# 4. math.log(x): Logaritmo natural de x.
# 5. math.log10(x): Logaritmo en base 10 de x.
# 6. math.sin(x): Seno de x (x en radianes).
# 7. math.cos(x): Coseno de x (x en radianes).
# 8. math.tan(x): Tangente de x (x en radianes).
# 9. math.fabs(x): Valor absoluto de x.
# 10. round(x): Redondea x al entero más cercano.
# 11. math.floor(x): Redondea x hacia abajo al entero más cercano.
# 12. math.ceil(x): Redondea x hacia arriba al entero más cercano.
# 13. math.factorial(x): Factorial de x.
# 14. math.comb(n, k): Combinación de n elementos tomados de k en k.
# 15. math.perm(n, k): Permutación de n elementos tomados de k en k.
# 16. math.pow(x, y): x elevado a la potencia y.
# 17. math.exp(x): Exponencial de x (e^x).
# 18. math.degrees(x): Convierte radianes a grados.
# 19. math.radians(x): Convierte grados a radianes.